// FunciÃ³n para generar configuraciones aleatorias pero realistas
function generateSensitivityConfig(deviceName, configNumber) {
  // Configuraciones mÃ¡s precisas y calibradas por tipo de dispositivo
  const deviceType = getDeviceType(deviceName)
  const baseConfigs = getConfigsByDeviceType(deviceType)

  const config = baseConfigs[configNumber - 1]
  const hasDPI = Math.random() > 0.4 // MÃ¡s dispositivos con DPI

  return {
    general: Math.floor(Math.random() * (config.general[1] - config.general[0] + 1)) + config.general[0],
    redDot: Math.floor(Math.random() * (config.redDot[1] - config.redDot[0] + 1)) + config.redDot[0],
    scope2x: Math.floor(Math.random() * (config.scope2x[1] - config.scope2x[0] + 1)) + config.scope2x[0],
    scope4x: Math.floor(Math.random() * (config.scope4x[1] - config.scope4x[0] + 1)) + config.scope4x[0],
    awm: Math.floor(Math.random() * (config.awm[1] - config.awm[0] + 1)) + config.awm[0],
    buttonSize: Math.floor(Math.random() * (config.buttonSize[1] - config.buttonSize[0] + 1)) + config.buttonSize[0],
    dpi: hasDPI ? Math.floor(Math.random() * 600) + 800 : "Sin DPI",
    refreshRate: config.refreshRate[Math.floor(Math.random() * config.refreshRate.length)] + "Hz",
  }
}

function getDeviceType(deviceName) {
  if (
    deviceName.includes("Gaming") ||
    deviceName.includes("ROG") ||
    deviceName.includes("Red Magic") ||
    deviceName.includes("Black Shark") ||
    deviceName.includes("Legion")
  ) {
    return "gaming"
  } else if (deviceName.includes("Pro") || deviceName.includes("Ultra") || deviceName.includes("Max")) {
    return "flagship"
  } else if (
    deviceName.includes("Lite") ||
    deviceName.includes("SE") ||
    deviceName.includes("Go") ||
    deviceName.includes("Play")
  ) {
    return "budget"
  } else {
    return "mid-range"
  }
}

