// Inicializar configuraciones para todos los dispositivos
function initializeSensitivityConfigs() {
  Object.keys(deviceDatabase).forEach((brand) => {
    Object.keys(deviceDatabase[brand]).forEach((series) => {
      deviceDatabase[brand][series].forEach((model) => {
        const deviceKey = `${brand}_${model}`
        sensitivityConfigs[deviceKey] = []

        for (let i = 1; i <= 8; i++) {
          sensitivityConfigs[deviceKey].push(generateSensitivityConfig(model, i))
        }
      })
    })
  })
}

// Variables globales
let currentDevice = null
let currentConfigs = []
let currentConfigIndex = 0

// Elementos del DOM
const brandSelect = document.getElementById("brand-select")
const modelSelect = document.getElementById("model-select")
const generateBtn = document.getElementById("generate-btn")
const regenerateBtn = document.getElementById("regenerate-btn")
const showAllBtn = document.getElementById("show-all-btn")
const configDisplay = document.getElementById("config-display")
const allConfigsDiv = document.getElementById("all-configs")

// Inicializar la aplicaciÃ³n
function initializeApp() {
  initializeSensitivityConfigs()
  populateBrandSelect()
  setupEventListeners()
}

// Poblar el selector de marcas
function populateBrandSelect() {
  const brands = Object.keys(deviceDatabase).sort()
  brands.forEach((brand) => {
    const option = document.createElement("option")
    option.value = brand
    option.textContent = brand
    brandSelect.appendChild(option)
  })
}

// Poblar el selector de modelos
function populateModelSelect(brand) {
  modelSelect.innerHTML = '<option value="">Selecciona un modelo</option>'

  if (!brand) {
    modelSelect.disabled = true
    generateBtn.disabled = true
    return
  }

  const models = []
  Object.keys(deviceDatabase[brand]).forEach((series) => {
    deviceDatabase[brand][series].forEach((model) => {
      models.push(model)
    })
  })

  models.sort().forEach((model) => {
    const option = document.createElement("option")
    option.value = model
    option.textContent = model
    modelSelect.appendChild(option)
  })

  modelSelect.disabled = false
}

// Configurar event listeners
function setupEventListeners() {
  brandSelect.addEventListener("change", (e) => {
    populateModelSelect(e.target.value)
    hideConfigDisplays()
  })

  modelSelect.addEventListener("change", (e) => {
    if (e.target.value) {
      generateBtn.disabled = false
      currentDevice = `${brandSelect.value}_${e.target.value}`
      currentConfigs = sensitivityConfigs[currentDevice]
    } else {
      generateBtn.disabled = true
      currentDevice = null
    }
    hideConfigDisplays()
  })

  generateBtn.addEventListener("click", generateRandomConfig)
  regenerateBtn.addEventListener("click", generateRandomConfig)
  showAllBtn.addEventListener("click", showAllConfigs)
}

// Generar configuraciÃ³n aleatoria
function generateRandomConfig() {
  if (!currentDevice || !currentConfigs) return

  currentConfigIndex = Math.floor(Math.random() * 8)
  const config = currentConfigs[currentConfigIndex]

  displayConfig(config, currentConfigIndex + 1)
  allConfigsDiv.style.display = "none"
}

// Mostrar configuraciÃ³n
function displayConfig(config, configNumber) {
  document.getElementById("device-name").textContent = modelSelect.value
  document.getElementById("config-number").textContent = `ConfiguraciÃ³n #${configNumber}`

  document.getElementById("general-value").textContent = config.general
  document.getElementById("red-dot-value").textContent = config.redDot
  document.getElementById("scope-2x-value").textContent = config.scope2x
  document.getElementById("scope-4x-value").textContent = config.scope4x
  document.getElementById("awm-value").textContent = config.awm
  document.getElementById("button-size-value").textContent = config.buttonSize
  document.getElementById("dpi-value").textContent = config.dpi
  document.getElementById("refresh-rate-value").textContent = config.refreshRate

  configDisplay.style.display = "block"
}

// Mostrar todas las configuraciones
function showAllConfigs() {
  if (!currentDevice || !currentConfigs) return

  const configsTable = document.getElementById("configs-table")
  configsTable.innerHTML = ""

  // Crear encabezado
  const headerRow = document.createElement("div")
  headerRow.className = "config-row"
  headerRow.innerHTML = `
        <div>Config</div>
        <div>General</div>
        <div>P. Rojo</div>
        <div>X2</div>
        <div>X4</div>
        <div>AWM</div>
        <div>BotÃ³n</div>
        <div>DPI</div>
        <div>Hz</div>
    `
  configsTable.appendChild(headerRow)

  // Crear filas de configuraciones
  currentConfigs.forEach((config, index) => {
    const row = document.createElement("div")
    row.className = "config-row"
    row.innerHTML = `
            <div>#${index + 1}</div>
            <div>${config.general}</div>
            <div>${config.redDot}</div>
            <div>${config.scope2x}</div>
            <div>${config.scope4x}</div>
            <div>${config.awm}</div>
            <div>${config.buttonSize}</div>
            <div>${config.dpi}</div>
            <div>${config.refreshRate}</div>
        `

    row.addEventListener("click", () => {
      displayConfig(config, index + 1)
      allConfigsDiv.style.display = "none"
    })

    configsTable.appendChild(row)
  })

  allConfigsDiv.style.display = "block"
}

// Ocultar displays de configuraciÃ³n
function hideConfigDisplays() {
  configDisplay.style.display = "none"
  allConfigsDiv.style.display = "none"
}

